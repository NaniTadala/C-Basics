//C Program to print various data types
#include<stdio.h>
void main()
{
	//Variable Declaration
	int age;
	double cgpa;
	char initial;
	
	//initialization
	age = 19;
	cgpa = 8.1;
	initial = 'T';
	
	//Output
	printf("Initial of the Student is: %c\n",initial);
	printf("Age of the Student is: %d\n",age);
	printf("CGPA of the Student is: %lf\n",cgpa);

}
